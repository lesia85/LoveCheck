TOTAL_QUESTIONS = len(SATISFACTION_QUESTIONS)

def get_progress(current: int) -> str:
    return f"Вопрос {current}/{TOTAL_QUESTIONS}"

async def start_satisfaction_survey(message: Message, state: FSMContext):
    # используется список для ответов, чтобы было легче считать среднее значение по индексам
    await state.update_data(satisfaction_answers=[], current_question=0)
    await ask_next_question(message, state)

async def ask_next_question(message: Message, state: FSMContext):
    data = await state.get_data()
    q_index = data.get("current_question", 0)

    if q_index < TOTAL_QUESTIONS:
        question = SATISFACTION_QUESTIONS[q_index]

        try:
            await message.edit_reply_markup(reply_markup=None)
        except:
            pass

        await message.answer(
            f"<b>{get_progress(q_index + 1)}</b>\n\n"
            f"{question}",
            parse_mode="HTML",
            reply_markup=get_rating_keyboard_by_5()
        )
    else:
        await finish_satisfaction_survey(message, state)

@router.message(StateFilter(UserStates.survey_satisfaction))
async def process_satisfaction_rating(message: Message, state: FSMContext):
    data = await state.get_data()
    q_index = data.get("current_question", 0)

    # проверка, что опрос еще идет
    if q_index < TOTAL_QUESTIONS:
        valid_ratings = {"1", "2", "3", "4", "5"}
        
        # корректная цифра
        if message.text in valid_ratings:
            rating = int(message.text)
            
            # список вопросов
            answers = data.get("satisfaction_answers", [])
            answers.append(rating)  # добавление оценку в конец списка

            # обновление вопроса
            await state.update_data(satisfaction_answers=answers, current_question=q_index + 1)
            await ask_next_question(message, state)
            
        # неправильный ввод пользователя 
        else:
            await message.answer("Пожалуйста, выберите оценку от 1 до 5 с помощью кнопок.")
            await ask_next_question(message, state)


